// ============================================================================
// Team — 团队
// ============================================================================
// 一组 Agent 的逻辑分组，有明确的汇报关系。
// 支持嵌套团队结构。Lead 拥有对 Members 的任务分配权。
// ============================================================================

/** 团队配置 */
export interface TeamConfig {
  /** 团队唯一标识 */
  id: string;
  /** 团队名称 */
  name: string;
  /** 团队负责人 Agent ID */
  lead: string;
  /** 成员 Agent ID 列表 */
  members: string[];
  /** 上级团队 ID（支持嵌套） */
  parentTeam?: string;
  /** 可委托的 Agent ID 列表 */
  allowDelegation?: string[];
  /** 团队描述 */
  description?: string;
}
